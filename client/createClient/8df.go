func extractUINs(code string) ([]string, error) {

	re := regexp.MustCompile(`"uin":(\d+)`)
	matches := re.FindAllStringSubmatch(code, -1)

	if len(matches) == 0 {
		return nil, nil
	}

	var uins []string
	for _, match := range matches {
		if len(match) >= 2 {
			uin, err := strconv.ParseUint(match[1], 10, 64)
			if err != nil {
				return nil, nil
			}
			uins = append(uins, strconv.Itoa(int(uin)))
		}
	}
	return uins, nil
}
